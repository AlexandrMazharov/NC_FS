class Book {
  _work;
  _yearPublication;
  _inventoryNumber;
  _bookHolder;
  _dateReturn;
  constructor(work, yearPublication, inventoryNumber, bookHolder, dateReturn) {
    this.work = work;
    this.yearPublication = yearPublication;
    this.inventoryNumber = inventoryNumber;
    this.bookHolder = bookHolder;
    this.dateReturn = dateReturn;
  }
}
